## Q1.Develop the R-Program for basic mathematics computation - Square, Square Root, Exponential etc.

# Square :
value_1 <- 16
print(value_1 ^ 2)
print(value_1 * value_1)
print(value_1 ** 2)

# Square Root :
value_2 <- 4
print(value_2 ^ 0.5)
print(sapply(value_2 , sqrt))
print(sqrt(value_2))

# Exponential :
value_3 <- 2
print(exp(value_3))
print(value_3 ^ 2)
print(value_3 ** 3)